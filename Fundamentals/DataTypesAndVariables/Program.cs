using System;

namespace DataTypesAndVariables
{
    class Program
    {
        static void Main(string[] args)
        {
            int  myInteger = 5;
            Console.WriteLine(myInteger);
            double myDouble = 5.25;
             Console.WriteLine(myDouble);
